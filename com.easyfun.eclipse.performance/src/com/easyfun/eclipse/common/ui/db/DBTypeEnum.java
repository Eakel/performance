package com.easyfun.eclipse.common.ui.db;

/**
 * 代表数据库URL的类型
 * 
 * @author linzhaoming
 *
 * 2013-12-26
 *
 */
public enum DBTypeEnum {
	Oracle("Oracle"),
	MySQL("MySQL");
	
	private String str;

	DBTypeEnum(String str) {
		this.str = str;
	}

	public String toString() {
		return str;
	}
	
	public String getName(){
		return str;
	}
}
